       ->join('subscriptions','subscriptions.user_user_id','=','users.user_id')->get();
       foreach($payments as &$payment){
           $payment->plan=DB::table('plans')->where('stripe_plan',$payment->stripe_plan)->first();
       }
       //dd($payments);
